class Solution {
public:
    vector<int> twoSum(vector<int>& nums, int target) {
        //Brute Force Approach
        //Time Complexity is O(n^2) for running two loops
        int n, i, j;
        n = nums.size();
        for (i=0; i<(n-1); i++){ //first element
            for (j=(i+1); j<n; j++){ //second element
                if(nums[i]+nums[j]==target){
                    return {i,j}; //return i and j
                }
            }
        }

        return {}; //else no solution
    }
};